    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        JFileChooser fileChoosen = new JFileChooser();
//            fileChoosen.setCurrentDirectory(new java.io.File("."));
        fileChoosen.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChoosen.setAcceptAllFileFilterUsed(false);
        if (fileChoosen.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            txtFilePath.setText(fileChoosen.getSelectedFile().getAbsolutePath());
            fileChoosen.cancelSelection();
            fileChoosen = new JFileChooser();
        }

    }//GEN-LAST:event_btnStartActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        if (txtFilePath.getText().trim().isEmpty() || txtFilePath.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Pleace choose a file in message");
            return;
        }
        if (this.jtaMessage.getText().trim().length() > 1) {
            try {
                System.out.println("Size: " + fileList.size());
                for (int i = start; i < fileList.size(); i++) {
                    file = new File(txtFilePath.getText().trim() + "\\" + fileList.get(i).fileName);
                    System.out.println(file.toString());
                    if (!file.exists()) {
                        fos = new FileOutputStream(file);
                        fos.write(fileList.get(i).data, 0, fileList.get(i).length);
                        start += 1;
                        JOptionPane.showMessageDialog(null, "Save file sucessful!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Please check directory again!");
                        return;
                    }         
                }
                fos.close();
            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "File context is not exist!");
            } catch (IOException ex) {
                ex.printStackTrace();
            }
            jtaMessage.setText("");
        }

    }//GEN-LAST:event_SaveActionPerformed

