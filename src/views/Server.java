    public void run() {
        while (true) {
            try {
                Socket fileClientServer = serverReceiveFile.accept();
                if (fileClientServer != null) {
                    sleep(400);
                    receiveFile = new ReceiveFileThread(fileClientServer, jtaMessage, fileList);
                    receiveFile.start();
                    fileExist = true;
                }
                sleep(500);
            } catch (Exception e) {

            }
        }
    }
